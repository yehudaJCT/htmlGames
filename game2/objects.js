

// Rectangle class for handling positions and dimensions
class Rectangle {
    constructor(x, y, width, height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}

// Player movement velocity
class velocity {
    constructor(x, y) {
        this.x = x;
        this.y = y;
    }
}